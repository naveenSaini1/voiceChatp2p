package com.example.demo.model;

public class Room {

	private String id;
	private String roomName;
	private Integer TotalParticate;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getRoomName() {
		return roomName;
	}
	public void setRoomName(String roomName) {
		this.roomName = roomName;
	}
	public Integer getTotalParticate() {
		return TotalParticate;
	}
	public void setTotalParticate(Integer totalParticate) {
		TotalParticate = totalParticate;
	}
	
	
	
}
